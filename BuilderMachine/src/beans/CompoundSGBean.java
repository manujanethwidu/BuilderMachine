package beans;

public class CompoundSGBean {

	double bsg;
	double csg;
	double trsg;
	
	
	public double getBsg() {
		return bsg;
	}
	public void setBsg(double bsg) {
		this.bsg = bsg;
	}
	public double getCsg() {
		return csg;
	}
	public void setCsg(double csg) {
		this.csg = csg;
	}
	public double getTrsg() {
		return trsg;
	}
	public void setTrsg(double trsg) {
		this.trsg = trsg;
	}
	
	
	
}
